package se.lexicon;

public class Ex4LowerCaseAndUpperCase {
    public static void main(String[] args){
        String string="CAPS EQUALS SCREAMING";
        string=string.toLowerCase();
        System.out.println("To LowerCase:"+string);
        string=string.toUpperCase();
        System.out.println("To UpperCase= " + string);
    }
}
